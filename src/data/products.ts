// Specs helpers
function panel(watts: number) {
  return [
    { label: 'Output', value: `${watts}W` },
    { label: 'Cell type', value: 'Monocrystalline (half-cell)' },
    { label: 'Price/W', value: 'KSh 22' },
  ]
}
function inverter(label: string, type = 'Hybrid', extra?: string) {
  return [
    { label: 'Capacity', value: label },
    { label: 'Type', value: type },
    { label: 'Output', value: 'Pure sine-wave' },
    ...(extra ? [{ label: 'Notes', value: extra }] : []),
  ]
}
function battery(capacity: string, voltage: string, type = 'LiFePO4 lithium') {
  return [
    { label: 'Capacity', value: capacity },
    { label: 'Voltage', value: voltage },
    { label: 'Chemistry', value: type },
  ]
}
function flood(watts: number, leds?: number, mAh?: number) {
  return [
    { label: 'Power', value: `${watts}W` },
    { label: 'IP rating', value: watts >= 300 ? 'IP66' : 'IP65' },
    ...(leds ? [{ label: 'LED chips', value: `${leds} pcs` }] : []),
    ...(mAh ? [{ label: 'Battery', value: `LiFePO4 ${mAh}mAh` }] : []),
  ]
}
function heater(litres: number, pressurized = false) {
  return [
    { label: 'Capacity', value: `${litres}L` },
    { label: 'Brand', value: 'AQUASUN' },
    { label: 'Type', value: pressurized ? 'Pressurized' : 'Non-pressurized' },
    { label: 'Includes', value: 'Assistant tank, controller, element, magnesium rod' },
  ]
}

