// Product builder helpers — keep specs minimal but informative.
function panel(watts: number): { label: string; value: string }[] {
  return [
    { label: 'Output', value: `${watts}W` },
    { label: 'Cell type', value: 'Monocrystalline' },
  ]
}
function inverter(kva: number, type = 'Hybrid'): { label: string; value: string }[] {
  return [
    { label: 'Capacity', value: kva < 1 ? `${kva * 1000}W` : `${kva}kVA` },
    { label: 'Type', value: type },
    { label: 'Output', value: 'Pure sine-wave' },
  ]
}
function battery(
  capacity: string,
  voltage: string,
  type: string,
): { label: string; value: string }[] {
  return [
    { label: 'Capacity', value: capacity },
    { label: 'Voltage', value: voltage },
    { label: 'Type', value: type },
  ]
}
function flood(watts: number): { label: string; value: string }[] {
  return [
    { label: 'Power', value: `${watts}W` },
    { label: 'IP rating', value: watts >= 800 ? 'IP66' : 'IP65' },
    { label: 'Battery', value: 'Built-in lithium' },
  ]
}
function ceiling(watts: number): { label: string; value: string }[] {
  return [
    { label: 'Power', value: `${watts}W` },
    { label: 'Mount', value: 'Surface' },
    { label: 'Panel', value: 'Split, included' },
  ]
}
function pump(depth?: number, type = 'Submersible'): { label: string; value: string }[] {
  return [
    { label: 'Type', value: type },
    ...(depth ? [{ label: 'Max head', value: `${depth}m` }] : []),
    { label: 'Powered by', value: 'Solar DC' },
  ]
}
function heater(litres: number, brand: string, pressurized: boolean): { label: string; value: string }[] {
  return [
    { label: 'Capacity', value: `${litres}L` },
    { label: 'Brand', value: brand },
    { label: 'Type', value: pressurized ? 'Pressurized' : 'Non-pressurized' },
  ]
}
function powerStation(watts: number, brand?: string): { label: string; value: string }[] {
  return [
    { label: 'Output', value: `${watts}W` },
    ...(brand ? [{ label: 'Brand', value: brand }] : []),
    { label: 'Type', value: 'Portable lithium' },
  ]
}

